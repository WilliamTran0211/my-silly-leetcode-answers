from typing import List


class Solution:
    def intersectionSizeTwo(self, intervals: List[List[int]]) -> int:
        res = 0
        sort_inter = sorted(intervals, key=lambda item: (item[1], -item[0]))

        p1 = -1
        p2 = -1

        for inter in sort_inter:
            start, end = inter
            if p2 < start:
                res += 1
                p1, p2 = end - 1, end
            elif p1 < start:
                res += 1
                p1, p2 = p2, end

        return res


